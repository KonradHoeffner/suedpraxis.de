<h1>Osteopathie</h1>

<p>
Die Osteopathie ist eine ganzheitliche Behandlungsmethode, bei der die Diagnose und die Therapie mit spezifischen manuellen Techniken erfolgt.
Sie basiert auf der genauen Kenntnis von Anatomie, Physiologie und Biochemie sowie dem Wissen um die Organisation und das Verhalten des Organismus im gesunden und kranken Zustand und während des Genesungsprozesses.
Der Grundsatz des Begründers der Osteopathie als Wissenschaft, Andrew Taylor Still
(* 1828 in Virginia; † 1917 in Missouri), lautet <i>&#8222;Leben ist Bewegung&#8220;</i>.
</br>
Es gilt herauszufinden, welche Strukturen in ihrer Bewegung eingeschränkt sind, dadurch mangelhaft versorgt werden und sich aufgrund der Unterversorgung krankhafte Symptome entwickeln.

Ist die Bewegungseinschränkung aufgehoben, kann der Körper über seine Selbstheilungskräfte in die Eigenregulation kommen und gesunden.

Auf der Grundlage dieses Konzeptes ist die Osteopathie für alle Altersgruppen, vom Säugling bis ins hohe Alter, geeignet.

 </p>
<br/>

<h4>Die Osteopathie basiert auf 3 Säulen</h4>
<ul>
<li>der parietalen,</li>
<li>der kraniosakralen,</li>
<li>der visceralen Osteopathie.</li>
</ul>

Parietale Osteopathie behandelt die gelenkigen Strukturen und ihre Verbindungen. Das sind Knochen, Faszien, Muskeln und Sehnen.
Die kraniosakrale Osteopathie beschäftigt sich mit dem Schädel, der Wirbelsäule, den Membranen und aus dem Schädel heraustretenden Strukturen, den Nerven.
Die viscerale Osteopathie beschäftigt sich mit Funktion, Versorgung und Bewegung der inneren Organe.
Auf allen drei Ebenen können Störungen vorkommen und sich gegenseitig beeinflussen.
Sie müssen gleichberechtigt untersucht und behandelt werden.
</p>
