<h1>Homöopathie</h1>
Die klassische Homöopathie basiert auf der wissenschaftlichen Arbeit von Dr. Samuel 
Hahnemann (* 1755 in Meißen; † 1843 in Paris).
Nach dem Prinzip <i>Ähnliches mit Ähnlichem</i> zu heilen werden Arzneimittel auf pflanzlicher, mineralischer und tierischer Basis verwendet, die im gesunden Organismus in hoher Dosierung Krankheitszustände ähnlich der bestehenden Symptomatik hervorrufen.
Durch Potenzierung werden die Arzneimittel verdünnt und dynamisiert, so dass sie keine toxische Wirkung mehr auf den Körper haben und die Selbstheilungskräfte im Menschen aktiviert werden.
Grundlage der Behandlung ist eine genaue Anamnese.
Sehr wichtig ist auch Ihre  Beobachtung der Symptomatik vor und nach der Mittelgabe. 
